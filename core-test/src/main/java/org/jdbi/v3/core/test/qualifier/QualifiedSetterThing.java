package org.jdbi.v3.core.test.qualifier;

import java.util.Objects;

public class QualifiedSetterThing {
    private int id;
    private String name;

    public QualifiedSetterThing() {}

    public QualifiedSetterThing(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    @Reversed
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        QualifiedSetterThing that = (QualifiedSetterThing) o;
        return id == that.id
            && Objects.equals(name, that.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name);
    }

    @Override
    public String toString() {
        return "QualifiedSetterThing{"
            + "id=" + id
            + ", name='" + name + '\''
            + '}';
    }
}
